-- ── Normaliza pontuação e prefixa DDD em clientes.telefone/whatsapp ────────────
-- Critério "já tem DDD": telefone com 10+ dígitos (depois de remover não-dígitos).
-- Critério "precisa de DDD": 8 dígitos (fixo) ou 9 dígitos (celular).

UPDATE clientes c
JOIN cidades_ddd_referencia r ON r.cidade = UPPER(c.cidade) AND r.uf = c.uf
SET c.telefone = CONCAT(
  '(', r.ddd, ') ',
  IF(
    LENGTH(REGEXP_REPLACE(c.telefone, '[^0-9]', '')) = 9,
    CONCAT(LEFT(REGEXP_REPLACE(c.telefone, '[^0-9]', ''), 5), '-', RIGHT(REGEXP_REPLACE(c.telefone, '[^0-9]', ''), 4)),
    CONCAT(LEFT(REGEXP_REPLACE(c.telefone, '[^0-9]', ''), 4), '-', RIGHT(REGEXP_REPLACE(c.telefone, '[^0-9]', ''), 4))
  )
)
WHERE c.telefone IS NOT NULL
  AND c.telefone <> ''
  AND LENGTH(REGEXP_REPLACE(c.telefone, '[^0-9]', '')) IN (8, 9);

