-- Migration 022: corrige telefones de clientes importados do vm2025 sem DDD.
-- O sistema legado guardava telefone/celular sem DDD — o DDD vinha implícito pela
-- cidade do cliente (tabela `cidade` do legado). A importação (importar-vm2025.ts)
-- não compôs o DDD no telefone, deixando a maioria dos registros sem DDD.
--
-- Heurística de correção (baseada em 2 pistas do usuário):
--   1. DDD vem da cidade/UF do cliente, via tabela de referência abaixo (extraída
--      do dump legado db/legado/migrado_de_vm2025.sql, tabela `cidade`).
--   2. Telefone só recebe DDD se ainda não tiver um: 8 dígitos = fixo (formato
--      "XXXX-XXXX"), 9 dígitos = celular (formato "XXXXX-XXXX"). Números com 10+
--      dígitos já têm DDD embutido e são apenas reformatados para "(XX) ...".
--
-- IMPORTANTE: cidade->DDD é só uma muleta para corrigir o dado legado, não uma
-- regra de negócio do vm2026 — um cliente pode morar numa cidade e ter telefone
-- com DDD de outra. Por isso a tabela de referência é temporária: criada, usada
-- nos UPDATEs abaixo e descartada (DROP) ao final desta mesma migration. Daqui
-- para frente, telefone sempre é digitado completo (com DDD) pelo usuário.

CREATE TABLE IF NOT EXISTS cidades_ddd_referencia (
  cidade VARCHAR(100) NOT NULL,
  uf CHAR(2) NOT NULL,
  ddd CHAR(2) NOT NULL,
  PRIMARY KEY (cidade, uf)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

